
# list comprehension to list multiples of 3 from 3 to 30
numbers = [number*3 for number in range(3, 30)]
print(numbers)
# using list comprehension to print cubes of 1 to 10
cubes = [cube**3 for cube in range(1,11)]
print(cubes)
# counting from 1 to 20 with for loop
for number in range(1,21):
	print(number)
